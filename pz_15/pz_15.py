
import sqlite3

conn = sqlite3.connect('химчистка.db')
cursor = conn.cursor()

cursor.execute('''CREATE TABLE IF NOT EXISTS Услуги
                  (ФИО_мастера TEXT,
                   ФИО_клиента TEXT,
                   Тип_чистки TEXT,
                   Стоимость REAL,
                   Скидка REAL)''')
conn.commit()

def ввод_данных():
  for _ in range(3):
    фио_мастера = input("ФИО мастера: ")
    фио_клиента = input("ФИО клиента: ")
    тип_чистки = input("Тип чистки: ")
    стоимость = float(input("Стоимость: "))
    скидка = float(input("Скидка: "))
    cursor.execute("INSERT INTO Услуги VALUES (?, ?, ?, ?, ?)", 
                   (фио_мастера, фио_клиента, тип_чистки, стоимость, скидка))
  conn.commit()

def поиск_данных():
  print("Варианты поиска:")
  print("1. По ФИО мастера")
  print("2. По ФИО клиента")
  print("3. По типу чистки")
  выбор = input("Выберите вариант поиска: ")
  if выбор == '1':
    фио_мастера = input("ФИО мастера: ")
    cursor.execute("SELECT * FROM Услуги WHERE ФИО_мастера=?", (фио_мастера,))
  elif выбор == '2':
    фио_клиента = input("ФИО клиента: ")
    cursor.execute("SELECT * FROM Услуги WHERE ФИО_клиента=?", (фио_клиента,))
  elif выбор == '3':
    тип_чистки = input("Тип чистки: ")
    cursor.execute("SELECT * FROM Услуги WHERE Тип_чистки=?", (тип_чистки,))
  else:
    print("Неверный выбор.")
    return
  результаты = cursor.fetchall()
  if результаты:
    for запись in результаты:
      print(запись)
  else:
    print("Записей не найдено.")

def удаление_данных():
  print("Варианты удаления:")
  print("1. По ФИО мастера")
  print("2. По ФИО клиента")
  print("3. По типу чистки")
  выбор = input("Выберите вариант удаления: ")
  if выбор == '1':
    фио_мастера = input("ФИО мастера: ")
    cursor.execute("DELETE FROM Услуги WHERE ФИО_мастера=?", (фио_мастера,))
  elif выбор == '2':
    фио_клиента = input("ФИО клиента: ")
    cursor.execute("DELETE FROM Услуги WHERE ФИО_клиента=?", (фио_клиента,))
  elif выбор == '3':
    тип_чистки = input("Тип чистки: ")
    cursor.execute("DELETE FROM Услуги WHERE Тип_чистки=?", (тип_чистки,))
  else:
    print("Неверный выбор.")
    return
  conn.commit()
  print("Записи удалены.")

def редактирование_данных():
  print("Варианты редактирования:")
  print("1. По ФИО мастера")
  print("2. По ФИО клиента")
  print("3. По типу чистки")
  выбор = input("Выберите вариант редактирования: ")
  if выбор == '1':
    фио_мастера = input("ФИО мастера (для поиска записи): ")
    новое_значение = input("Новое значение стоимости: ")
    cursor.execute("UPDATE Услуги SET Стоимость=? WHERE ФИО_мастера=?", (новое_значение, фио_мастера))
  elif выбор == '2':
    фио_клиента = input("ФИО клиента (для поиска записи): ")
    новое_значение = input("Новое значение скидки: ")
    cursor.execute("UPDATE Услуги SET Скидка=? WHERE ФИО_клиента=?", (новое_значение, фио_клиента))
  elif выбор == '3':
    тип_чистки = input("Тип чистки (для поиска записи): ")
    новое_значение = input("Новое значение ФИО мастера: ")
    cursor.execute("UPDATE Услуги SET ФИО_мастера=? WHERE Тип_чистки=?", (новое_значение, тип_чистки))
  else:
    print("Неверный выбор.")
    return
  conn.commit()
  print("Запись изменена.")


while True:
  print("\nМеню:")
  print("1. Ввод данных")
  print("2. Поиск данных")
  print("3. Удаление данных")
  print("4. Редактирование данных")
  print("5. Выход")
  выбор = input("Выберите действие: ")
  if выбор == '1':
    ввод_данных()
  elif выбор == '2':
    поиск_данных()
  elif выбор == '3':
    удаление_данных()
  elif выбор == '4':
    редактирование_данных()
  elif выбор == '5':
    break
  else:
    print("Неверный выбор.")

conn.close()
